    /*Initializes the filter creation menu that has been selected.*/
    private void DoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DoActionPerformed
        // TODO add your handling code here:
        if(checkTcp.isSelected()){
            CreateTcpFilter obj = new CreateTcpFilter();
            obj.toret = text;
            obj.setVisible(true);
            dispose();
        }else if(checkIp.isSelected()){
            CreateIpFilter obj = new CreateIpFilter();
            obj.toret = text;
            obj.setVisible(true);
            dispose();
        }else if(checkUdp.isSelected()){
            CreateUdpFilter obj = new CreateUdpFilter();
            obj.toret = text;
            obj.setVisible(true);
            dispose();
        }else if(checknDpi.isSelected()){
            nDpiFilter obj = new nDpiFilter();
            obj.toret = text;
            obj.setVisible(true);
            dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Select a type.", "Error", JOptionPane.ERROR_MESSAGE);
        } 
    }//GEN-LAST:event_DoActionPerformed

